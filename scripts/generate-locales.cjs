// ─── Generate ja.json and th.json from en.json structure ───
// For now, ja.json and th.json will have the same non-userManual keys as en.json
// (the t() function falls back to English for missing keys, so this is safe)
// We add the userManual section to all locale files

function addUserManualToLocale(baseObj, locale) {
  const result = { ...baseObj };
  result.userManual = userManualSections[locale];
  return result;
}

// Update en.json
const enWithManual = addUserManualToLocale(en, 'en');
fs.writeFileSync(
  path.join(localesDir, 'en.json'),
  JSON.stringify(enWithManual, null, 4) + '\n',
  'utf8'
);
console.log('✓ Updated en.json with userManual section');

// Update ko.json
const ko = JSON.parse(fs.readFileSync(path.join(localesDir, 'ko.json'), 'utf8'));
const koWithManual = addUserManualToLocale(ko, 'ko');
fs.writeFileSync(
  path.join(localesDir, 'ko.json'),
  JSON.stringify(koWithManual, null, 4) + '\n',
  'utf8'
);
console.log('✓ Updated ko.json with userManual section');

// Create jp.json (start with en as base for non-userManual keys, fallback handles the rest)
const jpWithManual = addUserManualToLocale(en, 'jp');
fs.writeFileSync(
  path.join(localesDir, 'jp.json'),
  JSON.stringify(jpWithManual, null, 4) + '\n',
  'utf8'
);
console.log('✓ Created jp.json with userManual section');

// Create th.json
const thWithManual = addUserManualToLocale(en, 'th');
fs.writeFileSync(
  path.join(localesDir, 'th.json'),
  JSON.stringify(thWithManual, null, 4) + '\n',
  'utf8'
);
console.log('✓ Created th.json with userManual section');

